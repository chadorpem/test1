class dog:
    def __init__(self,name,colour,age):
        self.name=name
        self.colour=colour
        self.age=age
        
dawa=dog('dawa','blue',2)
nima=dog('nima','black',1)
blacky=dog('blacky','yellow',3)
print(dawa.name)
print(dawa.colour)
print(dawa.age)
print(nima.name)
print(nima.colour)
print(nima.age)
print(blacky.name)
print(blacky.colour)
print(blacky.age)
